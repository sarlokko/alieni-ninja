  function drawSprite(ctx, sprite, x, y, scale, flipX) {
    if (!sprite) return;
    const s = scale || PX;
    const dw = sprite.w * s, dh = sprite.h * s;
    ctx.imageSmoothingEnabled = false;
    if (sprite.canvas) {
      ctx.save();
      if (flipX) { ctx.translate(x + dw, y); ctx.scale(-1, 1); ctx.drawImage(sprite.canvas, 0, 0, sprite.w, sprite.h, 0, 0, dw, dh); }
      else ctx.drawImage(sprite.canvas, 0, 0, sprite.w, sprite.h, x, y, dw, dh);
      ctx.restore(); return;
    }
    const { w, pixels } = sprite;
    for (let i = 0; i < pixels.length; i++) {
      const color = pixels[i]; if (!color) continue;
      const col = i % w, row = Math.floor(i / w);
      ctx.fillStyle = color;
      ctx.fillRect(x + (flipX ? w - 1 - col : col) * s, y + row * s, s, s);
    }
  }
  function drawSpriteCentered(ctx, sprite, cx, cy, scale, facingLeft) {
    if (!sprite) return;
    const s = scale || PX;
    drawSprite(ctx, sprite, cx - (sprite.w * s) / 2, cy - (sprite.h * s) / 2, s, facingLeft);
  }
  function drawPixelCircle(ctx, cx, cy, r, color) {
    ctx.fillStyle = color;
    const rr = Math.max(1, Math.round(r));
    for (let y = -rr; y <= rr; y++) for (let x = -rr; x <= rr; x++)
      if (x*x + y*y <= rr*rr) ctx.fillRect(Math.round(cx)+x, Math.round(cy)+y, 1, 1);
  }
  window.PixelSprites = { SPRITES, PX, drawSprite, drawSpriteCentered, drawPixelCircle, bakeSprite };
